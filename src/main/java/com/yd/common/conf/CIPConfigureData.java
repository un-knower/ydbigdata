package com.yd.common.conf;

public class CIPConfigureData {

	/**
	 * conf_file - 配置文件名
	 */
	private String conf_file;

	/**
	 * conf_id - 配置id
	 */
	private String conf_id;

	/**
	 * conf_value - 配置值
	 */
	private String conf_value;

	/**
	 * conf_value_desc - 配置值说明
	 */
	private String conf_value_desc;

	public String getConf_file() {
		return this.conf_file;
	}

	public void setConf_file(String conf_file) {
		this.conf_file = conf_file;
	}

	public String getConf_id() {
		return this.conf_id;
	}

	public void setConf_id(String conf_id) {
		this.conf_id = conf_id;
	}

	public String getConf_value() {
		return this.conf_value;
	}

	public void setConf_value(String conf_value) {
		this.conf_value = conf_value;
	}

	public String getConf_value_desc() {
		return this.conf_value_desc;
	}

	public void setConf_value_desc(String conf_value_desc) {
		this.conf_value_desc = conf_value_desc;
	}

}
