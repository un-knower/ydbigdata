package com.yd.common.function.mdm.data;

public class ProvinceData {
	private String provinceId;
	private String provinceName;
	private String provinceMiniName;
	private String provinceBigArea;
	
	public String getProvinceId() {
		return provinceId;
	}
	public void setProvinceId(String provinceId) {
		this.provinceId = provinceId;
	}
	public String getProvinceName() {
		return provinceName;
	}
	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	public String getProvinceMiniName() {
		return provinceMiniName;
	}
	public void setProvinceMiniName(String provinceMiniName) {
		this.provinceMiniName = provinceMiniName;
	}
	public String getProvinceBigArea() {
		return provinceBigArea;
	}
	public void setProvinceBigArea(String provinceBigArea) {
		this.provinceBigArea = provinceBigArea;
	}
	
	
}
