package com.yd.ydbi.business.model;

public class SitSucRate {
	private String orgName;
	private String orgCode;
	private String successRateTheDay;

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getOrgCode() {
		return orgCode;
	}

	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	public String getSuccessRateTheDay() {
		return successRateTheDay;
	}

	public void setSuccessRateTheDay(String successRateTheDay) {
		this.successRateTheDay = successRateTheDay;
	}
}
